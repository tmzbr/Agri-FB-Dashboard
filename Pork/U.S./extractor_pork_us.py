# MATERIALISE QUARTERLY
# ══════════════════════════════════════════════════════════════════════════════
def materialise_quarterly(conn):
    """Aggregate weekly rows into quarterly table."""
    ts = datetime.now().isoformat(timespec="seconds")
    rows = conn.execute(
        "SELECT report_date, carcass, hog_price, corn, sbm, fc_spot, "
        "       feed_grain_6m, spread_integrated, spread_non_integrated "
        "FROM weekly ORDER BY report_date"
    ).fetchall()

    # Group by quarter
    from collections import defaultdict
    qdata = defaultdict(lambda: {k: [] for k in
        ["carcass","hog_price","corn","sbm","fc_spot",
         "feed_grain_6m","spread_int","spread_nint"]})

    for r in rows:
        dt  = r[0]
        yr  = int(dt[:4])
        mo  = int(dt[5:7])
        q   = (mo - 1) // 3 + 1
        key = (yr, q)
        d   = qdata[key]
        if r[1] is not None: d["carcass"].append(r[1])
        if r[2] is not None: d["hog_price"].append(r[2])
        if r[3] is not None: d["corn"].append(r[3])
        if r[4] is not None: d["sbm"].append(r[4])
        if r[5] is not None: d["fc_spot"].append(r[5])
        if r[6] is not None: d["feed_grain_6m"].append(r[6])
        if r[7] is not None: d["spread_int"].append(r[7])
        if r[8] is not None: d["spread_nint"].append(r[8])

    def avg(lst): return sum(lst)/len(lst) if lst else None

    # Build quarterly rows with 6-month (2Q) lag for fc_6m
    quarters = sorted(qdata.keys())
    fc_spot_by_q = {k: avg(qdata[k]["fc_spot"]) for k in quarters}

    written = 0
    for i, (yr, q) in enumerate(quarters):
        d     = qdata[(yr, q)]
        label = f"{q}Q{str(yr)[2:]}"
        yq    = yr * 10 + q

        # fc_6m: use fc_spot from 2 quarters ago (≈6 months)
        fc_6m = fc_spot_by_q.get(quarters[i-2]) if i >= 2 else None

        carcass   = avg(d["carcass"])
        hog_price = avg(d["hog_price"])
        fg6m      = avg(d["feed_grain_6m"])

        # spread_integrated     = (Carcass/45.36) / (feed_grain_6m * 5.8)
        # spread_non_integrated  = (Carcass/Hog) / 45.36
        spread_int  = ((carcass / 45.36) / (fg6m * 5.8)) if (carcass and fg6m) else avg(d["spread_int"])
        spread_nint = ((carcass / hog_price) / 45.36)     if (carcass and hog_price) else avg(d["spread_nint"])

        conn.execute("""
            INSERT OR REPLACE INTO quarterly
            (quarter, year_q, year, q, carcass, hog_price, corn, sbm,
             fc_spot, fc_6m, spread_integrated, spread_non_integrated, updated_at)
            VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)
        """, (label, yq, yr, q, carcass, hog_price,
              avg(d["corn"]), avg(d["sbm"]), avg(d["fc_spot"]), fc_6m,
              round(spread_int,  4) if spread_int  else None,
              round(spread_nint, 4) if spread_nint else None,
              ts))
        written += 1

    conn.commit()
    n = conn.execute("SELECT COUNT(*) FROM quarterly").fetchone()[0]
    print(f"  quarterly: {n} rows materialised")

    # Print latest 4
    print(f"\n  {'Q':<7} {'Carcass':>9} {'Hog':>9} {'Sp_Int':>8} {'Sp_NonInt':>10}")
    for r in conn.execute(
        "SELECT quarter, carcass, hog_price, spread_integrated, spread_non_integrated "
        "FROM quarterly ORDER BY year_q DESC LIMIT 4"
    ).fetchall():
        def f(v): return f"{v:9.2f}" if v is not None else "      N/A"
        print(f"  {r[0]:<7} {f(r[1])} {f(r[2])} {f(r[3]):>8} {f(r[4]):>10}")


