#!/usr/bin/env python3
"""
extractor_pork_us.py — U.S. Pork Industry Spread Tracker
=========================================================
Builds / refreshes  pork_us.db  with weekly and quarterly spread data.

DATA SOURCES
  • Pork Carcass Cutout  → USDA AMS PDF  ams_2680.pdf  (weekly, LM_PK680)
                           MARS API fallback: report/2680 (requires USDA_API_KEY)
  • Hog Price (Neg B&G)  → USDA AMS PDF  lsddhps.pdf   (daily summary)
                           MARS API fallback: report/2675 (LM_HG203)
  • Corn Central IL $/bu → USDA AMS PDF  AMS_3192.pdf  (same as chicken tracker)
  • SBM Illinois $/ton   → USDA AMS PDF  ams_3511.pdf  (same as chicken tracker)
  • Historical seed      → Support_-_Weekly.xlsx, sheet "U.S. Spreads - Weekly"
                           (col D=date, F=feed_grain_6m USD/kg, G=hog USD/cwt,
                            H=carcass USD/cwt)  — Jan 2015 → Apr 2026

SPREAD DEFINITIONS
  • spread_integrated     = (Carcass / 45.36) / (feed_grain_6m × 5.8)
    → Integrated producers: own hog farms, cost is feedgrain
    → Carcass converted to USD/kg (/45.36); feed cost = grain USD/kg × FCR 5.8
    → FCR 5.8 = kg feed per kg liveweight (pork industry average)
    → Result: ~1.3–1.8×

  • spread_non_integrated = (Carcass / Hog Price) / 45.36
    → Non-integrated packers: buy live hogs on open market
    → (Carcass ÷ Hog) ratio then /45.36 → USD/kg packer margin
    → Result: ~0.015–0.030 USD/kg

  Feed grain basket: 66% Corn + 34% SBM
  Feed grain lag:    6 months (feed_grain_6m from seed; fc_6m from corn+sbm with 2Q lag)

UNITS
  Carcass & Hog prices:   USD/cwt
  feed_grain_6m:          USD/kg (pre-computed in seed)
  spread_integrated:      dimensionless ratio (~1.3–1.8×)
  spread_non_integrated:  USD/kg (~0.015–0.030)

USAGE
  pip install requests openpyxl pdfplumber
  python extractor_pork_us.py                       # incremental update
  python extractor_pork_us.py --seed PATH           # seed from xlsx file

OUTPUT
  pork_us.db  (SQLite)

SCHEMA — table: weekly
  report_date  TEXT PRIMARY KEY   ISO "YYYY-MM-DD"
  carcass      REAL               Pork carcass cutout USD/cwt
  hog_price    REAL               Negotiated Barrow & Gilt USD/cwt (national)
  corn         REAL               Corn Central IL $/bu
  sbm          REAL               SBM Illinois FOB $/ton
  fc_spot      REAL               Feed cost spot: 2.9802*corn + 0.03851*sbm ($/cwt)
  fc_6m        REAL               Feed cost 6-month lag (USD/cwt)
  feed_grain_6m REAL              Feed grain basket BRL equiv from seed (USD/kg)
  spread_integrated    REAL       carcass / fc_6m  (×)
  spread_non_integrated REAL      carcass / hog_price  (×)
  updated_at   TEXT               ISO timestamp

SCHEMA — table: quarterly
  quarter      TEXT PRIMARY KEY   e.g. "1Q17"
  year_q       INTEGER            sortable: 20171 …
  year         INTEGER
  q            INTEGER
  carcass      REAL               avg USD/cwt
  hog_price    REAL               avg USD/cwt
  corn         REAL               avg $/bu
  sbm          REAL               avg $/ton
  fc_spot      REAL               avg feed cost spot USD/cwt
  fc_6m        REAL               feed cost with 6Q lag
  spread_integrated    REAL       avg carcass / fc_6m
  spread_non_integrated REAL      avg carcass / hog_price
  updated_at   TEXT
"""

