# DATE PARSER (shared)
# ══════════════════════════════════════════════════════════════════════════════
def _parse_report_date(text: str) -> Optional[datetime]:
    for pat in [
        r"[Rr]eport\s+[Ff]or[:\s]+\d{1,2}/\d{1,2}/\d{4}\s+to\s+(\d{1,2}/\d{1,2}/\d{4})",
        r"[Ff]or\s+[Ww]eek\s+[Ee]nding[:\s]+(\d{1,2}/\d{1,2}/\d{4})",
        r"[Ww]eek\s+[Ee]nding[:\s]+(\d{1,2}/\d{1,2}/\d{4})",
        r"\b([A-Za-z]+ \d{1,2},?\s*\d{4})\b",
        r"\b(\d{1,2}/\d{1,2}/\d{4})\b",
    ]:
        m = re.search(pat, text)
        if m:
            raw = m.group(1).strip()
            for fmt in ("%m/%d/%Y", "%B %d, %Y", "%B %d %Y", "%b %d, %Y"):
                try:
                    return datetime.strptime(raw, fmt)
                except ValueError:
                    continue
    return None


def _first_price(text: str, min_v=1.0, max_v=9999.0) -> Optional[float]:
    for n in re.findall(r"\b(\d{1,4}\.\d{1,2})\b", text):
        v = float(n)
        if min_v <= v <= max_v:
            return v
    return None


