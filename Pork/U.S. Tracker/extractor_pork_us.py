# UPSERT WEEKLY
# ══════════════════════════════════════════════════════════════════════════════
def upsert_weekly(conn, rows: list[dict], label=""):
    """
    rows: list of dicts with keys from weekly table schema.
    Never overwrites non-NULL values (preserve seed data).
    """
    if not rows:
        return 0
    FIELDS = ["carcass", "hog_price", "corn", "sbm",
              "fc_spot", "fc_6m", "feed_grain_6m",
              "spread_integrated", "spread_non_integrated"]
    ts = datetime.now().isoformat(timespec="seconds")
    inserted = updated = 0

    for row in rows:
        dt = row["report_date"]
        existing = conn.execute(
            f"SELECT {','.join(FIELDS)} FROM weekly WHERE report_date=?", (dt,)
        ).fetchone()

        if existing is None:
            conn.execute(
                f"INSERT INTO weekly (report_date, {','.join(FIELDS)}, updated_at) "
                f"VALUES (?,{','.join(['?']*len(FIELDS))},?)",
                (dt, *[row.get(f) for f in FIELDS], ts)
            )
            inserted += 1
        else:
            updates = {f: row[f] for i, f in enumerate(FIELDS)
                       if existing[i] is None and row.get(f) is not None}
            if updates:
                conn.execute(
                    f"UPDATE weekly SET {','.join(f+'=?' for f in updates)}, updated_at=?"
                    f" WHERE report_date=?",
                    (*updates.values(), ts, dt)
                )
                updated += 1

    conn.commit()
    n = conn.execute("SELECT COUNT(*) FROM weekly").fetchone()[0]
    print(f"  {label}weekly: {n} rows (+{inserted} new, {updated} updated)")
    return inserted + updated


